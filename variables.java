class Main {
  public static void main(String[] args) {

    byte valor1 = 20;
    System.out.println("Valor: " + valor1);

    valor1 = 100;
    System.out.println("Novo Valor: "+ valor1);

    long valor2 = 10000000;
    System.out.println("Novo Valor: "+ valor2);

    double valor3 = 9.50110;
    System.out.println("Novo Valor: "+ valor3);

    float valor4= 12.684f;
    System.out.println("Novo Valor: "+ valor4);

    char valor5= 'f';
    System.out.println("Novo Valor: "+ valor5);

    boolean valor6 = false;
    System.out.println("Novo Valor: "+ valor6);

    String valor7 = "Nathan";
    System.out.println("Novo Valor: " + valor7);
  }
}